package ru.anatomica.cookstarter.entity;

public class Token {

    private String name;
    private String token;

    public Token() {
    }

    public Token(String name, String token) {
        this.name = name;
        this.token = token;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

}
